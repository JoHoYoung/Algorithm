//문제 2193 이친수
//0과 1로만 이루어진 수를 이진수라 한다. 이러한 이진수 중 특별한 성질을 갖는 것들이 있는데,
// 이들을 이친수(pinary number)라 한다. 이친수는 다음의 성질을 만족한다.
//
//이친수는 0으로 시작하지 않는다.
//이친수에서는 1이 두 번 연속으로 나타나지 않는다. 즉, 11을 부분 문자열로 갖지 않는다.
//예를 들면 1, 10, 100, 101, 1000, 1001 등이 이친수가 된다. 하지만 0010101이나
// 101101은 각각 1, 2번 규칙에 위배되므로 이친수가 아니다.
//
//N(1 ≤ N ≤ 90)이 주어졌을 때, N자리 이친수의 개수를 구하는 프로그램을 작성하시오.

#include<iostream>
#include<algorithm>
using namespace std;

//DP[N][0] : N의자리 이친수 중 0으로 끝나는것의 갯수
//DP[N][1] : N의자리 이친수 중 1로 끝나는 것의 갯수
//DP[N+1][0]=DP[N][1]+DP[N][0]
//DP[N+1][1]=DP[N][0];
//DP[N-1]과 DP[N]의 관계... DP[N-1]중 0으로끝나는것에 0,1, 1로 끝나는 것에 0
long DP[100][2];
int main() {

    int N;
    cin>>N;

    DP[0][0]=0;
    DP[0][1]=0;
    DP[1][0]=0;
    DP[1][1]=1;
    DP[2][0]=1;
    DP[2][1]=0;


    for(int i=3;i<=N;i++)
    {
        DP[i][0]=DP[i-1][0]+DP[i-1][1];
        DP[i][1]=DP[i-1][0];
    }

    cout<<DP[N][0]+DP[N][1];
    return 0;
}